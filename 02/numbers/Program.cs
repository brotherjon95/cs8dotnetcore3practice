// unsigned integer means positive whole number including 0
uint naturalNumber = 123;
Console.WriteLine(naturalNumber);
// integer means negative or positive whole number including 0
int integerNumber = -23;
Console.WriteLine(integerNumber);
// float means singe-precision floating point
// F suffix makes it a flaot literal
float relaNumber = 2.3F;
Console.WriteLine(relaNumber);
// double means double-precision floating point
double anotherRealNumber = 2.3; // double literal
Console.WriteLine(anotherRealNumber);